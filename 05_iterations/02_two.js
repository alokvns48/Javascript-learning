// while

let index = 0

while (index <= 10) {
    // console.log(`value of i is ${index}`)
    index += 2
}

let myArray = ["ironman", "hulk", "black widow"]

let arr = 0

while (arr < myArray.length) {
    // console.log(`value is ${myArray[arr]}`)
    arr++
}

// do while

let score = 11

do {
    console.log(`score is ${score}`)
    score++
}while (score <= 10)